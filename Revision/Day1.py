# 2. Programming Paradigms
#      - Procedural Programming
#      - Object-Oriented Programming (OOP)
#      - Functional Programming
#      - Event-Driven Programming
#      - Logic Programming

#  * Procedural Programming
#   - Based on the concept of procedure calls
#   - Examples: C, Pascal, Fortran
#  * Object-Oriented Programming (OOP)
#   - Based on the concept of objects that contain data and methods
#   - Examples: Java, C++, Python

#  * Functional Programming
#   - Based on the concept of mathematical functions
#   - Examples: Haskell, Lisp, Erlang

#  * Event-Driven Programming
#   - Based on the concept of events and event handlers
#   - Examples: JavaScript (in web development), C# (with .NET)

#  * Logic Programming
#   - Based on formal logic
#   - Examples: Prolog, Datalog

